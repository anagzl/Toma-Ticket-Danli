<h5>PREGUNTAS FRECUENTES SOBRE PATENTES</h5>
<ul style="text-align: justify;">
    <li style="text-align: justify;"><strong>¿Qué es una patente?</strong></li>
    Es el derecho especial que concede el Estado para la explotación comercial de una Invención.
</ul>
<div style="text-align: justify;">
    <ul style="text-align: justify;">
        <li><strong>¿Cómo pueden ser las invenciones?</strong></li>
        Las invenciones pueden ser de producto o procedimiento, donde interviene el ser humano con su intelecto, la cual aporta una nueva solución técnica a un problema.
    </ul>
</div>
<div style="text-align: justify;">
    <ul style="text-align: justify;">
        <li><strong>¿Qué protección tengo con una patente?</strong></li>
        La protección es que la invención no puede ser creada, utilizada, distribuida o vendida sin la autorización del titular o dueño de la Patente.
    </ul>
</div>
<div style="text-align: justify;">
    <ul>
        <li><strong>¿A qué tengo derecho con una patente?</strong></li>
        <ul style="text-align: justify;">1. El Titular o dueño tiene el derecho de una exclusividad de veinte (20) años de protección.</ul>
        <ul style="text-align: justify;">2. El Titular si así lo desea puede dar permiso, licencias e incluso de vender los Derechos de la invención a un tercero si así lo desea.</ul>
        <ul style="text-align: justify;">3. Cuando la Patente expira, expira la protección y toda la información técnica contenida en el documento pasa hacer de dominio público.</ul>
    </ul>
</div>
<div style="text-align: justify;">
    <ul>
        <li><strong>¿Por qué son necesarias e importantes las patentes?</strong></li>
        Constituyen en incentivos para sus creadores, ya que ofrecen reconocimiento y remuneración económica por las invenciones. Estos incentivos despiertan y desarrollan la innovación ya que se encuentran soluciones a problemas y estos contribuyen a mejorar la calidad de vida de los seres humanos.
    </ul>
</div>
<div style="text-align: justify;">
    <ul>
        <li><strong>¿Cómo y quién concede una patente?</strong></li>
        <ul>1. El primer paso será presentar la solicitud de patente, esta debe de venir acompañada por los antecedentes, descripción, reivindicaciones y todo material visual; todo deberá ser proporcionado en lenguaje claro y completo con suficientes detalles de manera que la información técnica quede descrita para su análisis y evaluación por parte de las examinadoras técnicas de patentes.</ul>
        <ul>2. Es la Dirección General de Propiedad Intelectual a través del departamento de patentes, es el encargado de verificar el cumplimiento de los requisitos para la concesión o denegación de la solicitud y así proveer protección territorial.</ul>
    </ul>
</div>
<div style="text-align: justify;">
    <ul>
        <li><strong>¿Cuáles son los tipos de patente y qué tiempo de protección tengo?</strong></li>
        <ul>Nuestra Legislación establece tres (3) tipos de patentes de Invenciones:</ul>
        <ul>1. Patente de Invención; veinte (20) años de protección.</ul>
        <ul>2. Modelo de Utilidad; quince (15) años de protección.</ul>
        <ul>3. Diseño Industrial; cinco (5) años de protección con dos (2) periodos renovables.</ul>
    </ul>
</div>
<div style="text-align: justify;">
    <ul>
        <li><strong>¿Se pueden presentar marcas, sistemas de software, canciones, etc.?</strong></li>
        NO, solamente pueden ser susceptibles de patente aquellas invenciones de producto o procedimiento que cumpla con los requisitos establecidos por la Ley de Propiedad Industrial, que son: novedad, nivel inventivo y aplicación industrial.
    </ul>
</div>
<div style="text-align: justify;">
    <ul>
        <li><strong>¿Puedo exponer y comercializar mi patente antes de presentar la solicitud?</strong></li>
        <ul>1. Lo ideal será presentar la solicitud de patente antes de divulgar o exponer la, en caso contrario esta será considerada como estado de la técnica (es definida como toda aquella información puesta a disposición del público ya sea por vía oral o escrita, y esto afectara uno de los requisitos, la novedad).</ul>
        <ul>2. En caso de que sea inevitable la divulgación de la Patente, por ejemplo, algún socio comercial o inversor, se recomienda que la divulgación sea acompañada por un acuerdo de confidencialidad.</ul>
    </ul>
</div>
<div style="text-align: justify;">
    <ul>
        <li><strong>¿Existe una patente mundial?</strong></li>
        De momento no existe ninguna patente mundial o patente internacional. La solicitud de patente debe ser presentada en cada país que le interese el mercado y desea tener protección, la misma será otorgada y tendrá validez en dicho país. En algunas Regiones aceptan y conceden patentes regionales; como ser la Oficina Europea de Patentes (OEP) y la Organización Regional Africana de la Propiedad Intelectual (ARIPO). Existe el Tratado en Cooperación en Materia de Patente (PCT), el cual es una herramienta que surte efecto de presentación simultánea en todos los países adheridos al convenio, la cual es una presentación internacional que proporciona el mismo efecto que una solicitud nacional, pero es cada país designado el encargado otorgar la patente, los requisitos sustantivos, los procedimientos para la concesión y tasas fiscales varían de país en país según la Legislación.
    </ul>
</div>
<div style="text-align: justify;">
    <ul>
        <li><strong>¿Puedo comentar con el personal de la oficina acerca de mi invención?</strong></li>
        Sí, el personal del departamento de Patentes de la Dirección General de Propiedad Intelectual está debidamente capacitado para orientarle de manera confidencial en cuál es el proceso a seguir para presentar una solicitud, esta información no es tomada como una divulgación oral o escrita.
    </ul>
</div>
<div style="text-align: justify;">
    <ul>
        <li><strong>¿Puedo volver a presentar una patente?</strong></li>
        No, las solicitudes de patente no pueden volver a presentarse debido a los tiempos establecidos internacionalmente para evaluar la novedad y nivel inventivo.
    </ul>
</div>
<div style="text-align: justify;">
    <ul>
        <li><strong>¿Qué pasa si no pago una anualidad?</strong></li>
        La falta de pago de una anualidad, después del tiempo de gracias (6 meses, con sobretasa) trae la caducidad a la solicitud o registro.
    </ul>
</div>
<div style="text-align: justify;">
    <ul>
        <li><strong>¿Cuánto tiempo dura el trámite de la patente?</strong></li>
        El trámite de una patente de invención o un modelo de utilidad durante todo el proceso desde su presentación a la concesión tiene una duración de dos años y medio considerando el tiempo de publicación (90 días hábiles). Los diseños industriales durante todo su proceso desde su presentación a la concesión tienen una duración de cinco meses considerando el tiempo de publicación (90 días hábiles).
    </ul>
</div>
<div style="text-align: justify;">
    <ul>
        <li><strong>¿Tendrá problema en comercializar una patente de la cual no soy dueño?</strong></li>
        Si la invención tiene un registro (patente) no podría ser utilizado, porque el tener este derecho permite que su titular excluya a un tercero de comercializar la invención Si la invención no tiene registro (patente) podrá ser utilizada, por tratarse de información de Dominio Público, ya que es la obtención del título o registro es lo que permite excluir a una tercera persona.
    </ul>
</div>
<div style="text-align: justify;">
    <ul>
        <li><strong>¿Cómo me doy cuenta si mi invención ya existe?</strong></li>
        Lo puede realizar mediante una búsqueda de antecedentes, que me permite conocer si mi patente se encuentra registrada en Honduras (Búsqueda Nacional) o en cualquier parte del mundo (Búsqueda Internacional), mediante la presentación de formulario con la tasa fiscal respectiva de L1000.00.
    </ul>
</div>
<div style="text-align: justify;">
    <ul>
        <li><strong>¿Existe algún descuento de las tasas establecidas?</strong></li>
        Si, cuando el solicitante es el propio autor o creador y su situación económica no le permite sufragar los gastos, puede invocar el Art. 14 de la Ley de Propiedad Intelectual lo que le permite tener una reducción del 90% el valor de las tasas fiscales.
    </ul>
</div>
<div>
    <ul style="text-align: justify;">
        <li><strong>¿Puedo patentar mis recetas de cocina, manera de hacer el café, manera de hacer jaleas, preparaciones de salsas?</strong></li>
        No. Las patentes de invenciones pueden registrarse si son de Producto y/o Procedimiento y que cumpla con los requisitos de Novedad y Nivel Inventivo a Nivel Mundial. Las patentes de Modelo de Utilidad pueden ser mejoras a maquinaria, artefacto, herramientas, objeto que proporcione una ventaja técnica que antes no tenía. Las patentes de Diseños Industriales proteger las apariencias y ornamentación de los objetos o productos.
    </ul>
</div>
