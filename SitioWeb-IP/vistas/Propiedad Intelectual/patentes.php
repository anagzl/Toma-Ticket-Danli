<p style="text-align: justify;">Constituye el título, certificado o documento oficial que emite el estado, a través de la Dirección General de Propiedad Intelectual, para acreditar los derechos exclusivos que corresponden al inventor de un nuevo producto o procedimiento que puede ser explotado industrialmente durante un espacio limitado de tiempo (veinte años desde la fecha de solicitud).
</p>
<p style="text-align: justify;">Nivel Inventivo: La invención debe de ser resultado de un esfuerzo creativo y no ser obvia para cualquier persona versada en la materia a la que corresponda la invención. Aplicación Industrial: La invención debe ser efectivamente realizable, funcional, operativa y capaz de ser utilizable en cualquier tipo de industria o actividad productiva. Que no haya existido en el estado de la técnica antes de la fecha de solicitud. DISEÑO INDUSTRIAL Combinación de líneas, formas y colores en dos dimensiones (bidimensional y tridimensional), que le dan un producto utilitario un aspecto ornamental o estético, susceptible a la aplicación industrial. MODELO DE UTILIDAD Cualquier forma, configuración o disposición de elementos de un objeto o de alguna parte del mismo, que permita un mejor o diferente funcionamiento del objeto que lo incorpora, o que le proporcione alguna utilidad, ventaja o efecto que antes no tenía.</p>
